// Un’espressione è un’unità di codice che produce un valore.
// Gli operatori combinano operandi (variabili, valori, espressioni) per creare espressioni.

// Esempi di operazioni aritmetiche:
let a = 5;
let b = 10;
let c = a + b;  // 15
console.log(c);

// Esempi di operatori logici e confronto:
let condizione = (a < b) && (b > 0);
console.log(condizione);  // true

// Negazione:
console.log(!false);  // true